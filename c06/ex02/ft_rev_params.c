#include <unistd.h>

int	main(int argc, char **argv)
{
	int	j;

	while (--argc > 0)
	{
		j = 0;
		while (argv[argc][j] != '\0')
			write(1, &argv[argc][j++], 1);
		write(1, &(char){'\n'}, 1);
	}
	return (0);
}
